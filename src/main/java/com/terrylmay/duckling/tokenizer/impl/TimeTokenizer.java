    public List<String> cut(String target) {
        Matcher match;
        int startline = -1, endline = -1;

        String[] temp = new String[99];
        int rpointer = 0;// 计数器，记录当前识别到哪一个字符串了
        List<String> tokens = new ArrayList<String>();

        match = PATTERN.matcher(target);
        boolean startmark = true;
        while (match.find()) {
            startline = match.start();
            if (endline == startline) {
                rpointer--;
                // 假如下一个识别到的时间字段和上一个是相连的则把下一个识别到的时间字段加到上一个时间字段去
                tokens.set(rpointer, tokens.get(rpointer) + match.group());
            } else {
                if (!startmark) {
                    rpointer--;
                    rpointer++;
                }
                startmark = false;
                // 记录当前识别到的时间字段，并把startmark开关关闭。这个开关貌似没用？
                tokens.add(rpointer, match.group());
            }
            endline = match.end();
            rpointer++;
        }
        if (rpointer > 0) {
            rpointer--;
            rpointer++;
        }
        return tokens;
    }
